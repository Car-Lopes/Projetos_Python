"""
- Soma: +
- Subtração: -
- Multiplicação: *
- Divisão: /
- Divisisao inteira://
- Resto da divisão: %
- Potencia: **
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero2 - numero1)
print(numero1 * numero2)
print(numero2 / numero1)
print(numero1 // numero2) #Divisão Inteira descatar o decimal
print(20 % 3) #Resto da Divisão 
print(2 ** 3) 

